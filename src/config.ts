export function calcMarginBottom({
  id,
  lucky,
  stage,
}: {
  id: string
  lucky: boolean
  stage: number
}) {
  if (!lucky && posY[id]) return posY[id]

  if (stage === 1) {
    return luckPosY[id] || 10
  }

  if (luckPosY[id]) {
    return luckPosY[id]
  }

  return 20
}
